func TestApikeyGenerate_FunctionKeyPersistedWhenTaskKeyFails(t *testing.T) {
	// Generating both keys: the function mint succeeds but the task mint fails.
	// The function key must already be persisted to state so it is not lost.
	resetApikeyFlags(t)

	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		if r.URL.Path == "/v1/keys" {
			// The task key is minted with the NVCT issuer; fail only that one.
			if r.Header.Get("Key-Issuer-Service") == "nvct-api" {
				w.WriteHeader(http.StatusInternalServerError)
				return
			}
			w.Header().Set("Content-Type", "application/json")
			w.WriteHeader(http.StatusCreated)
			_, _ = w.Write([]byte(`{"value":"nvapi-fn-key"}`))
			return
		}
		w.WriteHeader(http.StatusNotFound)
	}))
	defer srv.Close()

	configureApikeyTest(t, srv.URL)

	// Re-root state at the temp HOME so the save targets a temp file we can read
	// instead of the developer's real ~/.nvcf-cli.state.
	home := os.Getenv("HOME")
	origStateManager := state.DefaultStateManager
	state.DefaultStateManager = state.NewStateManager()
	t.Cleanup(func() { state.DefaultStateManager = origStateManager })

	apikeyFor = ""
	apikeyExpiresIn = "1h"

	err := runAPIKeyGenerate(nil, nil)
	require.Error(t, err, "operation must fail when the task key cannot be minted")
	assert.Contains(t, err.Error(), "task")

	raw, readErr := os.ReadFile(filepath.Join(home, ".nvcf-cli.state"))
	require.NoError(t, readErr, "state file must exist after the function key is minted")
	var saved struct {
		APIKey     string `json:"apiKey"`
		NVCTAPIKey string `json:"nvctApiKey"`
	}
	require.NoError(t, json.Unmarshal(raw, &saved))
	assert.Equal(t, "nvapi-fn-key", saved.APIKey, "function key must be persisted before the task failure")
	assert.Empty(t, saved.NVCTAPIKey, "task key must not be persisted")
}

func TestApikeyGenerate_ValidateRecordsPerKeyResults(t *testing.T) {
	// function key validation fails (403), task key validation passes (200).
	// Each key in the output must carry its own validated/validationError.
	resetApikeyFlags(t)

	srv := httptest.NewServer(http.HandlerFunc(func(w http.ResponseWriter, r *http.Request) {
		body, _ := io.ReadAll(r.Body)
		switch r.URL.Path {
		case "/v1/keys":
			w.Header().Set("Content-Type", "application/json")
			w.WriteHeader(http.StatusCreated)
			_, _ = w.Write([]byte(`{"value":"nvapi-fake-key"}`))
		case "/v1/introspect":
			var payload map[string]string
			_ = json.Unmarshal(body, &payload)
			if payload["audience_service_id"] == "nvcf-test-service-id" {
				w.WriteHeader(http.StatusForbidden)
			} else {
				w.WriteHeader(http.StatusOK)
			}
		default:
			w.WriteHeader(http.StatusNotFound)
		}
	}))
	defer srv.Close()

	configureApikeyTest(t, srv.URL)
	apikeyFor = ""
	apikeyValidate = true
	apikeyExpiresIn = "1h"

	out := captureGenerateJSON(t)

	keys, ok := out["keys"].([]interface{})
	require.True(t, ok, "output must contain a keys list")
	require.Len(t, keys, 2, "both function and task keys must be present")

	byService := map[string]map[string]interface{}{}
	for _, k := range keys {
		entry := k.(map[string]interface{})
		byService[entry["service"].(string)] = entry
	}

	fn := byService["function"]
	require.NotNil(t, fn, "function key must be present")
	assert.Equal(t, false, fn["validated"], "function key validation must fail")
	assert.NotEmpty(t, fn["validationError"], "failed key must carry a validationError")

	task := byService["task"]
	require.NotNil(t, task, "task key must be present")
	assert.Equal(t, true, task["validated"], "task key validation must pass")
}

func TestApikeyGenerate_OutputIncludesPerKeyScopes(t *testing.T) {
	// Without --scopes, each key reports its service default scopes.
	resetApikeyFlags(t)
	srv, _, _ := fakeAPIKeysServer(t)
	configureApikeyTest(t, srv.URL)

	apikeyFor = ""
	apikeyExpiresIn = "1h"

	out := captureGenerateJSON(t)

	keys, ok := out["keys"].([]interface{})
	require.True(t, ok, "output must contain a keys list")
	require.Len(t, keys, 2)

	byService := map[string][]string{}
	for _, k := range keys {
		entry := k.(map[string]interface{})
		var scopes []string
		for _, s := range entry["scopes"].([]interface{}) {
			scopes = append(scopes, s.(string))
		}
		byService[entry["service"].(string)] = scopes
	}

	assert.Equal(t, apikeys.DefaultScopes(), byService["function"], "function key must report function default scopes")
	assert.Equal(t, apikeys.DefaultNVCTScopes(), byService["task"], "task key must report task default scopes")
}

// captureGenerateJSON runs runAPIKeyGenerate in JSON mode and returns the
// parsed stdout object.
func captureGenerateJSON(t *testing.T) map[string]interface{} {
	t.Helper()
	jsonOutput = true
	t.Cleanup(func() { jsonOutput = false })

	oldStdout := os.Stdout
	r, w, err := os.Pipe()
	require.NoError(t, err)
	os.Stdout = w

	require.NoError(t, runAPIKeyGenerate(nil, nil))

	require.NoError(t, w.Close())
	os.Stdout = oldStdout

	var buf bytes.Buffer
	_, err = io.Copy(&buf, r)
	require.NoError(t, err)

	var out map[string]interface{}
	require.NoError(t, json.Unmarshal(buf.Bytes(), &out))
	return out
}
