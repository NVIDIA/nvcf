func runShowAPIKey(cmd *cobra.Command, args []string) error {
	// Load state
	if err := LoadStateForCurrentCommand(); err != nil {
		logging.Warning("Could not load existing state: %v", err)
	}

	currentState := GetCurrentState()

	if currentState.APIKey == "" {
		if IsJSONOutput() {
			return OutputJSON(map[string]interface{}{
				"hasKey": false,
			})
		}
		logging.Info("No API key found in state")
		logging.Plain("Use 'nvcf-cli api-key generate' to create one")
		return nil
	}

	if IsJSONOutput() {
		return OutputJSON(map[string]interface{}{
			"hasKey":    true,
			"apiKey":    currentState.APIKey,
			"expiresAt": currentState.APIKeyExpiration,
		})
	}

	logging.Success("Current saved API key:")
	logging.Plain("API Key: %s", currentState.APIKey)

	if !currentState.APIKeyExpiration.IsZero() {
		if time.Now().After(currentState.APIKeyExpiration) {
			logging.Warning("Expires: %s (EXPIRED)", currentState.APIKeyExpiration.Format("2006-01-02 15:04:05 MST"))
		} else {
			logging.Plain("Expires: %s", currentState.APIKeyExpiration.Format("2006-01-02 15:04:05 MST"))
		}
	}

	return nil
}

func runClearAPIKey(cmd *cobra.Command, args []string) error {
	// Load state
	if err := LoadStateForCurrentCommand(); err != nil {
		logging.Warning("Could not load existing state: %v", err)
	}

	currentState := GetCurrentState()

	if currentState.APIKey == "" {
		if IsJSONOutput() {
			return OutputJSON(map[string]interface{}{
				"status": "no_key",
			})
		}
		logging.Info("No API key found in state to clear")
		return nil
	}

	// Confirmation prompt unless --force is used
	if !clearForce {
		logging.Warning("This will remove the API key from local state.")
		logging.Plain("The key itself remains valid but the CLI will no longer use it automatically.")
		logging.Plain("")

		if !promptForConfirmation("Are you sure you want to clear the saved API key?") {
			logging.Info("Clear cancelled.")
			return nil
		}
	}

	// Clear the API key from state
	SetCurrentTokens(currentState.Token, "", currentState.TokenExpiration, time.Time{})
	if err := SaveStateForCurrentCommand(); err != nil {
		return fmt.Errorf("failed to save cleared state: %w", err)
	}
	if IsJSONOutput() {
		return OutputJSON(map[string]interface{}{
			"status": "cleared",
		})
	}

	logging.Success("API key cleared from state")
	return nil
}

func runClearAllAPIKeys(cmd *cobra.Command, args []string) error {
	// Load configuration
	config, err := client.LoadConfig()
	if err != nil {
		return fmt.Errorf("failed to load configuration: %w", err)
	}

	// Determine owner ID
	ownerID := getConfigValueWithDefault("API_KEYS_OWNER_ID", "svc@nvcf-api.local")
	if len(args) > 0 && args[0] != "" {
		ownerID = args[0]
	}

	logging.Info("Fetching all API keys for owner: %s", ownerID)

	ctx := context.Background()

	// Create API Keys service client
	apiKeysConfig := &apikeys.Config{
		ServiceURL:    getConfigValueWithDefault("api_keys_service_url", "https://api-keys.nvcf.nvidia.com"),
		ServiceID:     getConfigValueWithDefault("API_KEYS_SERVICE_ID", "nvidia-cloud-functions-ncp-service-id-aketm"),
		IssuerService: getConfigValueWithDefault("API_KEYS_ISSUER_SERVICE", "nvcf-api"),
		OwnerID:       ownerID,
		HostHeader:    getConfigValueWithDefault("api_keys_host", ""), // Host header for self-hosted routing
		Debug:         config.Debug,
	}

	apiKeysClient := apikeys.NewClient(apiKeysConfig)

	// List all API keys for this owner
	keys, err := apiKeysClient.ListAPIKeys(ctx)
	if err != nil {
		return fmt.Errorf("failed to list API keys: %w", err)
	}

	if len(keys) == 0 {
		if IsJSONOutput() {
			return OutputJSON(map[string]interface{}{
				"ownerId": ownerID,
				"deleted": 0,
				"total":   0,
			})
		}
		logging.Info("No API keys found for owner: %s", ownerID)
		return nil
	}

	logging.Info("Found %d API key(s) to delete", len(keys))

	// Interactive confirmation prompt unless --force is used
	if !clearForce {
		logging.Warning("Are you sure you want to delete ALL %d API key(s) for owner '%s'?", len(keys), ownerID)
		logging.Plain("This action cannot be undone. All keys will be immediately invalidated.")
		logging.Plain("Keys to be deleted:")
		for _, key := range keys {
			logging.Plain("  - %s (%s)", key.ID, key.Description)
		}

		if !promptForConfirmation("Do you want to proceed?") {
			logging.Info("Bulk deletion cancelled by user")
			return nil
		}
	}

	// Delete all keys
	deletedCount := 0
	for _, key := range keys {
		logging.Info("Deleting key: %s (%s)", key.ID, key.Description)
		err = apiKeysClient.DeleteAPIKey(ctx, key.ID)
		if err != nil {
			logging.Error("Failed to delete key %s: %v", key.ID, err)
		} else {
			logging.Success("Deleted key: %s", key.ID)
			deletedCount++
		}
	}
	if IsJSONOutput() {
		return OutputJSON(map[string]interface{}{
			"ownerId": ownerID,
			"deleted": deletedCount,
			"total":   len(keys),
		})
	}

	logging.Success("Successfully deleted %d out of %d API key(s) for owner: %s", deletedCount, len(keys), ownerID)

	// Clear local API key if we deleted keys for the current configured owner
	currentConfigOwner := getConfigValueWithDefault("API_KEYS_OWNER_ID", "svc@nvcf-api.local")
	if ownerID == currentConfigOwner && deletedCount > 0 {
		// Load and clear state
		if err := LoadStateForCurrentCommand(); err == nil {
			currentState := GetCurrentState()
			if currentState.APIKey != "" {
				SetCurrentTokens(currentState.Token, "", currentState.TokenExpiration, time.Time{})
				if err := SaveStateForCurrentCommand(); err != nil {
					logging.Warning("Failed to clear local API key from state: %v", err)
				} else {
					logging.Info("Cleared local API key from state since it may have been deleted")
				}
			}
		}
	}

	return nil
}

// parseExtendedDuration parses duration strings with support for days (d) and weeks (w)
func parseExtendedDuration(s string) (time.Duration, error) {
	// First try Go's standard parser
	if d, err := time.ParseDuration(s); err == nil {
		return d, nil
	}

	// Handle days and weeks manually
	if len(s) < 2 {
		return 0, fmt.Errorf("invalid duration format")
	}

	unit := s[len(s)-1:]
	valueStr := s[:len(s)-1]

	switch unit {
	case "d":
		// Parse days
		days, err := strconv.Atoi(valueStr)
		if err != nil {
			return 0, fmt.Errorf("invalid number of days: %s", valueStr)
		}
		return time.Duration(days) * 24 * time.Hour, nil

	case "w":
		// Parse weeks
		weeks, err := strconv.Atoi(valueStr)
		if err != nil {
			return 0, fmt.Errorf("invalid number of weeks: %s", valueStr)
		}
		return time.Duration(weeks) * 7 * 24 * time.Hour, nil

	default:
		// Try Go's parser one more time and return its error
		return time.ParseDuration(s)
	}
}

// promptForConfirmation asks the user for yes/no confirmation
// Returns true if user confirms (y/yes), false otherwise
func promptForConfirmation(message string) bool {
	reader := bufio.NewReader(os.Stdin)

	fmt.Printf("%s (y/n): ", message)

	response, err := reader.ReadString('\n')
	if err != nil {
		return false
	}

	response = strings.ToLower(strings.TrimSpace(response))
	return response == "y" || response == "yes"
}
